def t(key: str, lang: str, **kwargs: Any) -> str:
    """
    Возвращает локализованный текст.
    lang: "ua" или "ru"
    kwargs: подстановки для .format()
    """
    lang = lang if lang in ("ua", "ru") else "ua"
    template = TEXTS.get(key, {}).get(lang, f"[{key}]")
    try:
        return template.format(**kwargs) if kwargs else template
    except (KeyError, IndexError):
        return template
